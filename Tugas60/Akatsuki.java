package Tugas60;

public class Akatsuki {
	 protected String pakaian;
	    protected String tujuan;

	    public Akatsuki(String pakaian, String tujuan) {
	        this.pakaian = pakaian;
	        this.tujuan = tujuan;
	    }

	    public String getpakaian() {
	        return pakaian;
	    }

	    public String gettujuan() {
	        return tujuan;
	    }


}
